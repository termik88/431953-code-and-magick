'use strict';

(function () {
  var setup = document.querySelector('.setup');
  var setupWizard = setup.querySelector('.setup-wizard');
  var setupWizardCoat = setupWizard.querySelector('.wizard-coat');
  var setupWizardEyes = setupWizard.querySelector('.wizard-eyes');
  var setupFireballWrap = setup.querySelector('.setup-fireball-wrap');

  var fillElement = function (element, color) {
    element.style.fill = color;
  };

  var changeElementBackground = function (element, color) {
    element.style.backgroundColor = color;
  };

  /* Редактирование персонажа */
  setupWizardEyes.addEventListener('click', function () {
    window.colorizeElement(setupWizardEyes, window.PARAMETERS_MAGES.EYES_COLORS, setupWizardEyes.style.fill, fillElement);
  });

  setupWizardCoat.addEventListener('click', function () {
    window.colorizeElement(setupWizardCoat, window.PARAMETERS_MAGES.COAT_COLORS, setupWizardCoat.style.fill, fillElement);
  });

  setupFireballWrap.addEventListener('click', function () {
    window.colorizeElement(setupFireballWrap, window.PARAMETERS_MAGES.FIREBALL_COLORS, window.rgbToHex(setupFireballWrap.style.backgroundColor), changeElementBackground);
  });

})();
